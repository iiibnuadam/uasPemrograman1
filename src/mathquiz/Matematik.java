package mathquiz;

public class Matematik {
    int temp;
	public int tambah(int a, int b) {
		return a + b;
	}
	public int kurang(int a, int b) {
		return a - b;
	}
	public int kali(int a, int b) {
		return a * b;
	}
	public int kelilingPersegPanjang(int a, int b) {
		return 2 * (a + b);
	}
	public int luasSegitiga(int a, int b) {
		return a * b / 2;
	}
}
